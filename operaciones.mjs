function suma(num1, num2){
    const result = num1 + num2;
    return result;
}

function resta(num1, num2){
    const result = num1 - num2;
    return result;
}

function mult(num1, num2){
    const result = num1 * num2;
    return result;
}

function div(num1, num2){
    const result = num1 / num2;
    return result;
}

export {
    suma, resta, mult, div
};